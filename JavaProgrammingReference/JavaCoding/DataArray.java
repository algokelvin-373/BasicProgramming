public class DataArray {
    public static void main(String[] args) {
        // Data Array Number
        int[] dataNum = {1, 2, 3, 4, 5};

        // Use looping to get data array
        for (int x = 0; x < dataNum.length; x++) {
            System.out.println(dataNum[x] + " ");
        }
        System.out.println();
    }
}
