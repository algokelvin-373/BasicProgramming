package com.youtube.java_code._03_Operator;

public class Java_003_Operator {
    public static void main(String[] args) {
        // Operator fundamentals.others.Arithmetic: +, -, *, /
        int a = 10, b = 5;
        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);

        // Operator Logical --> || (OR) && (AND) ! (NOT)
        boolean x = true, y = false;
        System.out.println(x && y);
        System.out.println(x || y);
        System.out.println(!x);
        System.out.println(!y);

    }
}
