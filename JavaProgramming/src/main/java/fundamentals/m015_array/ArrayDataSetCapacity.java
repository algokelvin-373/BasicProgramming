public class ArrayDataSetCapacity {
    public static void main(String[] args) {
        int[] arrNumber = new int[3];
        String[] arrString = new String[4];

        arrNumber[0] = 100;
        arrNumber[1] = 200;
        arrNumber[2] = 300;
        System.out.println("Array Number:");
        System.out.print(arrNumber[0]+" ");
        System.out.print(arrNumber[1]+" ");
        System.out.print(arrNumber[2]+" ");
        System.out.println();

        arrString[0] = "Java";
        arrString[1] = "Kotlin";
        arrString[2] = "Dart";
        arrString[3] = "Golang";
        System.out.println("Array Number:");
        System.out.print(arrString[0]+" ");
        System.out.print(arrString[1]+" ");
        System.out.print(arrString[2]+" ");
        System.out.print(arrString[3]+" ");
        System.out.println();

    }
}
