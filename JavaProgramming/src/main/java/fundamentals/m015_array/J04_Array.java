public class J04_Array {
    public static void main(String[] args) {
        
        // Data Array Number
        int[] dataNum = {1, 2, 3, 4, 5};

        // Use loop to get data array
        for (int x = 0; x < dataNum.length; x++) {
            System.out.print(dataNum[x] + " ");
        }
        System.out.println();

    }
}
