package material.m04;

public class ArithmeticProgram {
    public static void main(String[] args) {
        // 3, 7, 11, 15, 19  --> n = 5, b = 4

        // Looping
        int num = 3;
        for (int index = 1; index <= 5; index++) {
            System.out.print(num+" ");
            num = num + 4;
        }

    }
}
