package com.youtube.java_code._06_LoopingFor;

public class Java_006_LoopingFor {
    public static void main(String[] args) {
        // Show: 2, 4, 6, 8, 10

        // Looping 'for'
        for (int x = 2; x <= 10; x+=2) {
            System.out.print(x+" ");
        }
        System.out.println();

    }
}
