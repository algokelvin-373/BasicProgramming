public class ArrayDataInitializer {
    public static void main(String[] args) {
        int[] arrNumber = {1, 2, 3, 4, 5}; // type: number
        String[] arrString = {"Java", "Kotlin", "Dart", "Golang"}; // type: string

        System.out.println("Array Number:");
        System.out.print(arrNumber[0]+" ");
        System.out.print(arrNumber[1]+" ");
        System.out.print(arrNumber[2]+" ");
        System.out.print(arrNumber[3]+" ");
        System.out.print(arrNumber[4]+" ");
        System.out.println();

        System.out.println("Array String:");
        System.out.print(arrString[0]+" ");
        System.out.print(arrString[1]+" ");
        System.out.print(arrString[2]+" ");
        System.out.print(arrString[3]+" ");
        System.out.println();
    }
}
