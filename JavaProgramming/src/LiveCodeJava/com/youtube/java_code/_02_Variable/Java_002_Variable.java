package com.youtube.java_code._02_Variable;

public class Java_002_Variable {
    public static void main(String[] args) {
        // Type Data Integer
        int typeInt = 10;

        // Type Data Float
        float typeFloat = 3.14f;

        // Type Data Double
        double typeDouble = 3.14;

        // Type Data character
        char typeChar = 'k';

        // Type Data String
        String typeString = "AlgoKelvin";

        System.out.println(typeInt);
        System.out.println(typeFloat);
        System.out.println(typeDouble);
        System.out.println(typeChar);
        System.out.println(typeString);
    }
}
