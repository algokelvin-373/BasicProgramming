package com.codingday;

public class CodingJavaDay4 {
    public static void main(String[] args) {
        int a = 10, b = 5;

        int plus = a + b;
        int minus = a - b;
        int times = a * b;
        int divide = a / b;
        int modulo = a % 3;

        System.out.println(a+" + "+b+" = "+ plus);
        System.out.println(a+" - "+b+" = "+ minus);
        System.out.println(a+" * "+b+" = "+ times);
        System.out.println(a+" / "+b+" = "+ divide);
        System.out.println(a+" % 3 = "+modulo);
    }
}
