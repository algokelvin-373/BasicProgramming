package com.codingday;

public class CodingJavaDay7 {
    public static void main(String[] args) {
        int number = 10;
        System.out.println("Number = "+ number);

        number += 5;
        System.out.println("Number now 1 = "+ number);

        number -= 5;
        System.out.println("Number now 2 = "+ number);

        number *= 5;
        System.out.println("Number now 3 = "+ number);

        number /= 5;
        System.out.println("Number now 4 = "+ number);

    }
}
