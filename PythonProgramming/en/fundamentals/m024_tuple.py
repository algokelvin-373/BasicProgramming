tuples = (23, 'Kelvin', 17.5, 2 + 3j, 'Python')

print(tuples)
print(f'length: {len(tuples)}')
print(f'tuples[1]     : {tuples[1]}')     # index 1
print(f'tuples[2:4]   : {tuples[2:4]}')   # index 2 until  3
print(f'tuples[2:]    : {tuples[2:]}')    # index 2 until last
print(f'tuples[-4]    : {tuples[-4]}')    # index 1
print(f'tuples[-4:-1] : {tuples[-3:-1]}') # index 2 until  3
print(f'tuples[-4:]   : {tuples[-3:]}')   # index 2 until last
