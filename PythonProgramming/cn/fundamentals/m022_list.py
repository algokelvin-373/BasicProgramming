list_num = [4, 8, 12, 16, 20, 24, 28, 32, 36, 40]
print('-----[List]-----')
print(list_num)
print(f'length           : {len(list_num)}')   # length = 10
print(f'list_num[1]      : {list_num[1]}')     # index 1
print(f'list_num[2:5]    : {list_num[2:5]}')   # index 2 until 4
print(f'list_num[8:]     : {list_num[8:]}')    # index 8 until last
print(f'list_num[-9]     : {list_num[-9]}')    # index 1
print(f'list_num[-8:-5]  : {list_num[-8:-5]}') # index 2 until 4
print(f'list_num[-2:]    : {list_num[-2:]}')   # index 8 until last
