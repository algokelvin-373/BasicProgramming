print("dataTuple[2:] = ", dataTuple[2:])    # Index 2 - last

print("dataTuple[-4] = ", dataTuple[-4])        # Index 0
print("dataTuple[-3:-1] = ", dataTuple[-3:-1])  # Index 1 - 3
print("dataTuple[-2:] = ", dataTuple[-2:])      # Index 2 - last
