
//Operator Relational 2
void main() {
  int i = 8, j = 7, l = 9;
  print('* Operator Relational : ">" -> Bigger than');
  print('* $i > $j -> ${i > j}');
  print('* $i > $l -> ${i > l}');
  print('* ------------------------');
  print('* Operator Relational : "<" -> Smaller than');
  print('* $i < $j -> ${i < j}');
  print('* $i < $l -> ${i < l}');
}
