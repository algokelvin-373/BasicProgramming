
//Operator Relational 1
void main() {
  int i = 8, j = 7, k = 8;
  print('* Operator Relational : "==" -> Equal to');
  print('* $i == $j -> ${i == j}');
  print('* $i == $k -> ${i == k}');
  print('* ------------------------');
  print('* Operator Relational : "!=" -> Not Equal to');
  print('* $i != $j -> ${i != j}');
  print('* $i != $k -> ${i != k}');
}
