
//Operator Arithmetic
void main() {
  int a = 3, b = 4;
  print('$a + $b = ${a + b}'); // plus
  print('$a - $b = ${a - b}'); // minus
  print('$a * $b = ${a * b}'); // times
  print('$b / $a = ${b / a}'); // divided by
  print('$b % $a = ${b % a}'); // modulo

  double c = 25;
  print('$c ~/ $a = ${c ~/ a}'); // divided by for double
}
