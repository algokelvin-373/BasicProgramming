
//Operator Decrement
void main() {
  int y = 2;

  //Pre-Decrement "--y" - Minus 1, then, print data
  print('Value y before: $y');
  print('Pre-decrement : ${--y}');
  print('Value y after : $y');

  //Post-Decrement "y--" - Print data, then, minus 1
  print('Value y before: $y');
  print('Post-increment : ${y--}');
  print('Value y after : $y');
}
