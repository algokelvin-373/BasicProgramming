//Operator Relational 3
void main() {
  int i = 8, j = 7, k = 8, l = 9;
  print('* Operator Relational : ">=" -> Bigger or equal to');
  print('* $i >= $j -> ${i > j}');
  print('* $i >= $k -> ${i >= k}');
  print('* $i >= $l -> ${i > l}');
  print('* ------------------------');
  print('* Operator Relational : "<=" -> Smaller or equal to');
  print('* $i <= $j -> ${i < j}');
  print('* $i <= $k -> ${i <= k}');
  print('* $i <= $l -> ${i < l}');
}