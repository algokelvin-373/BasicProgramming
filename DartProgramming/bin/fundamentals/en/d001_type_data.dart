void main() {
  int dataInt = 100;
  int dataIntInHex = 0xFF;
  double dataDouble = 3.14;
  String dataStr = "My name is Kelvin";
  bool dataBool = true;

  // Show Output
  print("Data Integer       : " + dataInt.toString());
  print("Data Integer (Hex) : " + dataIntInHex.toString());
  print("Data Double        : " + dataDouble.toString());
  print("Data String        : " + dataStr.toString());
  print("Data Boolean       : " + dataBool.toString());
}
