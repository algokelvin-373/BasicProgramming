package fundamentals._02_DataType

fun main() {
    // Type 'integer'
    val typeInt: Int = 100
    println("Type Integer: $typeInt")

    // Type 'float'
    val typeFloat: Float = 3.14f
    println("Type Float: $typeFloat")

    // Type 'double'
    val typeDouble: Double = 3.14
    println("Type Double: $typeDouble")

    // Type 'boolean'
    val typeBoolean: Boolean = true
    println("Type Boolean: $typeBoolean")

    // Type 'character'
    val typeChar: Char = 'k'
    println("Type Char: $typeChar")

    // Type 'string'
    val typeString: String = "AlgoKelvin"
    println("Type String: $typeString")
}