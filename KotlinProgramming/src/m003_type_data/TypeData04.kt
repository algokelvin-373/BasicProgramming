package m003_type_data

fun main() {
    // Type Data 4 - Number Specific variable

    val x1: Int? = 10 // Int
    val x2: Double? = 3.14 // Double
    val x3: Float? = 9.1f // Float
    val x4: Double? = 5e+5 // Exponential
    val x5: Long? = 500L // Long
    println("$x1 $x2 $x3")
    println("$x4 $x5")
}
